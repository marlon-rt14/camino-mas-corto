    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        // TODO add your handling code here:
        crearTabla();
        DefaultTableModel dtm = (DefaultTableModel) tblGrafo.getModel();
        for (int i = 0; i < matriz.length; i++) {
            for (int j = 1; j < matriz[i].length; j++) {
                if (dtm.getValueAt(i, j).equals("")) {
                    matriz[i][j] = 0;
                }else{
                    matriz[i][j] = Integer.parseInt(dtm.getValueAt(i, j).toString());
                }
            }
        }
        
        for (int i = 0; i < matriz.length; i++) {
            for (int j = 1; j < matriz[i].length; j++) {
                System.out.print(matriz[i][j] + " ");
            }
            System.out.println("");
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

    public void crearTabla() {
        int cantidad = Integer.parseInt(txtCantidad.getText());
        Tabla tabla = new Tabla(cantidad, (DefaultTableModel) tblGrafo.getModel());
        tblGrafo.setModel(tabla.getModelo());
        tblGrafo.setFillsViewportHeight(true);
        tblGrafo.setForeground(Color.black);
        //tblGrafo.setShowVerticalLines(true);
        //tblGrafo.setShowGrid(true);
        //tblGrafo.sets
        //tblGrafo.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
        DefaultTableModel dtm = (DefaultTableModel) tblGrafo.getModel();
        int tamanio = tabla.getColumnas().length;
        matriz = new int[tamanio - 1][tamanio];
        String header[] = tabla.getColumnas();
        
        int cont = 0;
        for (int i = 1; i < tamanio; i++) {
            String aux[] = new String[tamanio];
            aux[0] = header[i];
            for (int j = 1; j < tamanio; j++) {
                //aux[j] = String.valueOf(cont);
                aux[j] = "";
                cont++;
            }
            dtm.addRow(aux);
        }
    }

